package polymorphism;

public class SamsungTv implements Tv{
	public SamsungTv() {
		System.out.println("==> 삼성TV 객체 생성");
	}
	
	public void powerOn() {
		System.out.println("SamsngTV---전원 켠다.");
	}
	
	public void powerOff() {
		System.out.println("SamsngTV---전원 끈다.");
	}
		
	public void volumeUp() {
		System.out.println("SamsngTV---소리 올린다.");
	}
	
	public void volumeDown() {
		System.out.println("SamsngTV---소리 내린다.");
	}
}